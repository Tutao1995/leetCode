/**
 * Definition for a binary tree node.
 * function TreeNode(val) {
 *     this.val = val;
 *     this.left = this.right = null;
 * }
 */
/**
 * @param {TreeNode} root
 * @return {number[]}
 */
var inorderTraversal = function(root) {
  if(!root) return [];
  let res = [];
  let put = (root) => {
    if(root) {
      put(root.left);
      res.push(root.val);
      put(root.right)
    }
  }
  put(root);
  return res;
};